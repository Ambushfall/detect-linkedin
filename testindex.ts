  liExtensions: string[];
  liExtensionLinks: string[];
  liSuccessExtensions: Response[] = [];
  intSuccessC: number = 0;
  intFailC: number = 0;
  constructor (extensions: string[]) {
    this.liExtensions = extensions
    this.liExtensionLinks = extensions.map(
      item => `https://chromewebstore.google.com/detail/${item}`
    )
    this.processFetches()
  }

  async processFetches () {
    let done = false
    let promiseList = this.liExtensionLinks.map(item => fetch(item));
    (await Promise.allSettled(promiseList)).map((response, index) => {
      if (response.status === 'fulfilled' && response.value !== undefined) {
        this.liSuccessExtensions.push(response.value)
        // console.log("success")
        // this.intSuccessC++;
        if (!done) {
          
          done = true
        }

        return true
      } else {
        // console.log("fail");
        // this.intFailC++;
        return false
      }
    });
  }

  
